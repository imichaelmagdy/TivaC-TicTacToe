void startingScreen(){
	Nokia5110_Clear();
	Nokia5110_SetCursor(0, 0);
	Nokia5110_OutString("Tic-Tac-Toe!");
	Nokia5110_SetCursor(0, 1);
	Nokia5110_OutString("Designed by:");
	Nokia5110_SetCursor(0, 2);
	Nokia5110_OutString("Michael");
	Nokia5110_SetCursor(4, 3);
	Nokia5110_OutString("Magdy");
	Nokia5110_SetCursor(1, 4);
	Nokia5110_OutString("Press any");
	Nokia5110_SetCursor(3, 5);
	Nokia5110_OutString("key...");
	xScore = 0;
	oScore = 0;
	while(SW1 != 0 && SW2 != 0){
	}
}

void initBoard(){
	Nokia5110_Clear();
	Nokia5110_PrintBMP(0, 47, emptyBoard, 0);
	Nokia5110_DisplayBuffer();
	Delay100ms(10);
	startGame();
}

void startGame(){
	int i = 0;
	for (i=0;i<9;i++){
		board[i] = 0;
	}
	whoseTurn = 'X';
	toggle = 0;
	cursorLocation = 0;
	roundCounter = 0;
	isWinner = 0;
	while(1){
		toggleSelector();
		if (SW1 == 0){
			while (SW1 == 0){Delay100ms(1);}
			moveCursorNext();
		}
		if (SW2 == 0){
			while (SW2 == 0){Delay100ms(1);}
			commitLetter();
		}
		checkResult();
	}
}

void moveCursorNext(){
	printLetter(' ');
	do {
		cursorLocation++;
		if (cursorLocation > 8){
			cursorLocation = 0;}
		} while(board[cursorLocation] != 0);
}

void commitLetter(){
	printLetter(whoseTurn);
	board[cursorLocation] = whoseTurn;
	if (whoseTurn == 'X'){whoseTurn='O';}
	else if (whoseTurn == 'O'){whoseTurn='X';}
	cursorLocation = calculateCursor();
	roundCounter++;
}

void checkResult(){
	if ( board[0] == board[1] && board[1] == board[2] && board[2] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[3] == board[4] && board[4] == board[5] && board[5] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[6] == board[7] && board[7] == board[8] && board[8] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[0] == board[3] && board[3] == board[6] && board[6] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[1] == board[4] && board[4] == board[7] && board[7] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[2] == board[5] && board[5] == board[8] && board[8] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[0] == board[4] && board[4] == board[8] && board[8] != 0 ){isWinner = 1; declareResult(board[0]);}
	if ( board[2] == board[4] && board[4] == board[6] && board[6] != 0 ){isWinner = 1; declareResult(board[0]);}
	
	if (roundCounter > 8){declareResult(' ');}
}

void declareResult(char winner){
	if (isWinner == 1){
		Nokia5110_Clear();
		Nokia5110_SetCursor(3, 0);
		if (winner == 'X'){Nokia5110_OutString("X wins"); xScore++;}
		else if (winner == 'O'){Nokia5110_OutString("O wins"); oScore++;}
	}
	else{
		Nokia5110_Clear();
		Nokia5110_SetCursor(4, 0);
		Nokia5110_OutString("Draw");
	}
	Nokia5110_SetCursor(2, 1);
	Nokia5110_OutString("X:");
	Nokia5110_SetCursor(5, 1);
	Nokia5110_OutUDec(xScore);
	Nokia5110_SetCursor(2, 2);
	Nokia5110_OutString("O:");
	Nokia5110_SetCursor(5, 2);
	Nokia5110_OutUDec(oScore);
	Nokia5110_SetCursor(1, 3);
	Nokia5110_OutString("Press any");
	Nokia5110_SetCursor(3, 4);
	Nokia5110_OutString("key to");
	Nokia5110_SetCursor(1, 5);
	Nokia5110_OutString("play again");
	while(SW1 != 0 && SW2 != 0){}
	initBoard();
}

void printLetter(char letter){
	xPosition = 16*(cursorLocation%3) + 2;
	yPosition = 16*(cursorLocation/3) + 13;
	Nokia5110_PrintBMP(xPosition, yPosition, emptySquare, 0);
	if (letter == 'X'){
		Nokia5110_PrintBMP(xPosition, yPosition, letterX, 0);
	}else if(letter == 'O'){
		Nokia5110_PrintBMP(xPosition, yPosition, letterO, 0);
	}else if(letter == ' '){
		Nokia5110_PrintBMP(xPosition, yPosition, emptySquare, 0);
	}
	Nokia5110_DisplayBuffer();
}

int calculateCursor(){
	int i = 0;
	for (i=0;i<9;i++){
		if (board[i] == 0){
			return i;
		}
	}
	return -1;
}

void toggleSelector(){
	xPosition = 16*(cursorLocation%3) + 2;
	yPosition = 16*(cursorLocation/3) + 13;
	if (toggle == 0){
		Nokia5110_PrintBMP(xPosition, yPosition, selectPointer, 0);
		toggle = 1;
	}else if (toggle == 1){
		Nokia5110_PrintBMP(xPosition, yPosition, emptySquare, 0);
		toggle = 0;
	}
	Nokia5110_DisplayBuffer();
	Delay100ms(1);
}

void PortF_Init(void){ volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000020;     // 1) F clock
  delay = SYSCTL_RCGC2_R;           // delay   
  GPIO_PORTF_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0  
  GPIO_PORTF_CR_R = 0x1F;           // allow changes to PF4-0       
  GPIO_PORTF_AMSEL_R = 0x00;        // 3) disable analog function
  GPIO_PORTF_PCTL_R = 0x00000000;   // 4) GPIO clear bit PCTL  
  GPIO_PORTF_DIR_R = 0x0E;          // 5) PF4,PF0 input, PF3,PF2,PF1 output   
  GPIO_PORTF_AFSEL_R = 0x00;        // 6) no alternate function
  GPIO_PORTF_PUR_R = 0x11;          // enable pullup resistors on PF4,PF0       
  GPIO_PORTF_DEN_R = 0x1F;          // 7) enable digital pins PF4-PF0 
}


void Delay100ms(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = 727240;  // 0.1sec at 80 MHz
    while(time){
	  	time--;
    }
    count--;
  }
}
