#define SW1 (GPIO_PORTF_DATA_R & 0x10)
#define SW2 (GPIO_PORTF_DATA_R & 0x01)

int xPosition, yPosition;
unsigned short xScore, oScore;
int cursorLocation;
char whoseTurn;
int roundCounter;
char toggle;
char isWinner;
char board[9];
